/**nos ayuda a obtener los datos internos de una coleccion */
/**no sirve par ainterar dentro de un objeto para eso es el for in  */
/* solo se maneja por arreglos*/

//**for(let-valor-of-arreglo) {operacion}*/

var arreglo = [1,2,3,4,5,6]
for (let valor of arreglo) {
    console.log(valor);
}

//**se puede interar un string  */

var nombre = "francisco"
for (let valor of nombre) {
    console.log(valor);
}
